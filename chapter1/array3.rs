fn main() {
    let ints = [1, 2, 3, 4, 5];
    let floats = [1.1, 2.1, 3.1, 4.1];
    let strings = ["hello", "world", "!"];
    let ints_ints = [[1, 2], [10, 20], [100, 200]];
    println!("ints {:?}", ints);
    println!("floats {:?}", floats);
    println!("strings {:?}", strings);
    println!("ints_ints {:?}", ints_ints);
}
